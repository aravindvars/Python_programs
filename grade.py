marks = int(input('Student\'s mark is:'))

if marks >= 90:
    print('S grade')
elif marks >= 80:
    print('A grade')
elif marks >= 70:
    print('B grade')
elif marks >= 60:
    print('C grade')
elif marks >= 50:
    print('D grade')
elif marks >= 40:
    print('E grade')
else:
    print('F grade')
